namespace VirtuMath;

/// <summary>
/// Enum for the format of representing a Number as a string.
/// </summary>
public enum FormatEnum
{
    /// <summary>
    /// The default format.
    /// </summary>
    None,
    /// <summary>
    /// Represents the Number as plain text.
    /// </summary>
    PlainText,
    /// <summary>
    /// Represents the Number in a tag format.
    /// </summary>
    Tags,
    /// <summary>
    /// Represents the Number in markdown.
    /// </summary>
    Markdown
}
